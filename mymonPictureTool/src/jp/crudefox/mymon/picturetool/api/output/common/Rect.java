public final class Rect {


    public int left;
    public int top;
    public int right;
    public int bottom;

    @Override
    public String toString() {
        return "Rect{" +
                "left=" + left +
                ", top=" + top +
                ", right=" + right +
                ", bottom=" + bottom +
                '}';
    }



}
